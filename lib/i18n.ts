export function detectLocale(): Locale {
  if (typeof window === "undefined") return "ru"

  const browserLang = navigator.language.toLowerCase()

  if (browserLang.startsWith("zh")) return "zh"
  if (browserLang.startsWith("en")) return "en"

  return "ru"
}
